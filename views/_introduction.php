        </ul>
        <h3>好處</h3>
        <ul>
            <li>每個人開發邏輯一致、花點時間了解概念都可以快速上手。</li>
            <li>所以工程師可以專注於單一 JavaScript 功能的開發、也容易分工。</li>
            <li>模組可以獨立作業、增加或減少都不會對其他模組造成衝突。</li>
            <li>模組便於單獨 Unit Test。</li>
        </ul>
